package Learning1;

public class Vehicle {
    int id;
    String type;
    String brand;
    String model;
    int year;
    boolean isAvailable;

    public Vehicle(int id, String type, String brand, String model, int year, boolean available) {
        this.id = id;
        this.type = type;
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.isAvailable = available ;

    }
}

